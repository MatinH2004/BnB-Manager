CREATE TABLE apartments (
  id serial PRIMARY KEY,
  "name" text NOT NULL,
  "address" text NOT NULL
);

CREATE TABLE tenants (
  id serial PRIMARY KEY,
  "name" text NOT NULL,
  rent numeric,
  apartment_id int NOT NULL REFERENCES apartments(id) ON DELETE CASCADE
);

